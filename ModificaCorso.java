    // eseguito quando si clicca il bottone "Carica" e recupera i dati inseriti:
    private void ModificaActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_CaricaActionPerformed

        // Controlla se i campi sono vuoti o non validi, colora il bordo in rosso se mancano dati, mostra messaggi dâ€™errore e verifica che la durata sia numerica (Integer.parseInt con try-catch)

        String codice = InputCorsoCodice.getText();
        String nome = InputCorsoNome.getText();
        String durata = InputCorsoDurata.getText();

        Integer durata_int;

        boolean b_codice = false, b_nome = false, b_durata = false;

        if (codice.equals("")) {
            InputCorsoCodice.setBorder(new LineBorder(Color.red, 3));
            b_codice = false;
            ErrorText.setText("Parametro/i mancante/i");
        } else {
            InputCorsoCodice.setBorder(new LineBorder(Color.black, 1));
            b_codice = true;
        }

        if (nome.equals("")) {
            InputCorsoNome.setBorder(new LineBorder(Color.red, 3));
            b_nome = false;
            ErrorText.setText("Parametro/i mancante/i");
        } else {
            InputCorsoNome.setBorder(new LineBorder(Color.black, 1));
            b_nome = true;
        }

        if (durata.equals("")) {
            InputCorsoDurata.setBorder(new LineBorder(Color.red, 3));
            b_durata = false;
            ErrorText.setText("Parametro/i mancante/i");
        } else {
            boolean d_i = true;

            try {
                durata_int = Integer.parseInt(durata);
            } catch (NumberFormatException e) {
                d_i = false;
            }

            if (d_i) {
                b_durata = true;
                InputCorsoDurata.setBorder(new LineBorder(Color.black, 1));
            } else {
                if (!codice.equals("") && !nome.equals(""))
                    ErrorText.setText("E' richiesto un valore numerico");
                InputCorsoDurata.setBorder(new LineBorder(Color.red, 3));
            }
        }

        if (b_codice && b_nome && b_durata) {
            //chiama il metodo aggiungiCorso del Gestore e chiude la finestra.
            H.getGestore().modificaCorso(codice, nome, Integer.parseInt(durata));
            H.getText().setText(H.getGestore().stampaCorsi());
            this.setVisible(false);
        }
    }// GEN-LAST:event_CaricaActionPerformed

